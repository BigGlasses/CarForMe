package Objects;

public class VehicleDataPoint {
	public String model;
	public String make;
	public String fuelType;
	public String fuelType1;

	public VehicleDataPoint(String model, String make, String fuelType) {
		this.model = model;
		this.make = make;
		this.fuelType = fuelType;
		this.fuelType1 = "";
	}
}
